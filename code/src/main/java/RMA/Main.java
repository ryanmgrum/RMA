package RMA;

/** Main is used to launch the Java VM and then load the JavaFX runtime
 *  (for some reason, a class that extends {@link javafx.application.Application}
 *  is unable to load).
 */
public class Main {
    /** This main method is what is initially loaded by the JVM.
     * @param args Any {@link String} arguments passed in on the command-line.
     */
    public static void main(String[] args){RMA.main(args);}
}
